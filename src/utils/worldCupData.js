export function getCurrentRound() {
  const now = Date.now();
  for (let i = GROUP_ROUND_DATES.length - 1; i >= 0; i--) {
    const r = GROUP_ROUND_DATES[i];
    if (now >= new Date(r.startDate + 'T00:00:00Z').getTime() && now < new Date(r.endDate + 'T23:59:59Z').getTime()) {
      return r;
    }
  }
  if (GROUP_ROUND_DATES.length > 0 && now < new Date(GROUP_ROUND_DATES[0].startDate + 'T00:00:00Z').getTime()) {
    return GROUP_ROUND_DATES[0];
  }
  return null;
}

export function getDaysInRound(roundIndex) {
  const r = GROUP_ROUND_DATES[roundIndex];
  if (!r) return [];
  const start = new Date(r.startDate + 'T00:00:00Z');
  const end = new Date(r.endDate + 'T00:00:00Z');
  const days = [];
  for (let d = new Date(start); d <= end; d.setDate(d.getDate() + 1)) {
    days.push(d.toISOString().split('T')[0]);
  }
  return days;
}

